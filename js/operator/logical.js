console.log("logical/relational operator");
//Logical operator 3 types
// 1. and(&&):all condition is true.

var a = 10;
var b = 20;

console.log(a < b && b > a); //true
console.log(a < b && b == a); //false
//2.or(||):at least one condition is true
console.log(a < b || b == a); //true

//3.Not(!)
var a = true;
console.log(!a); //false
